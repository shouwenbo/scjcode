        static TranslateHelper()
        {
            string[] lines = HANWENXINGMINGCOMMONTRANSLATE.Split(new[] { '\n', '\r' }, StringSplitOptions.RemoveEmptyEntries);

            // 遍历每一行
            foreach (var line in lines)
            {
                // 使用制表符分割汉字和韩文
                string[] parts = line.Split('\t');

                // 确保行格式正确，避免出现分割失败的情况
                if (parts.Length == 2)
                {
                    string chinese = parts[0].Trim();
                    string korean = parts[1].Trim();
                    HanwenXingCommonDictionary.TryAdd(chinese, korean);
                }
            }
        }

